import Foundation
import UIKit

extension UIColor {
    
    class func paleGray() -> UIColor {
        return UIColor(red: 236.0/255.0, green: 236.0/255.0, blue: 237.0/255.0, alpha: 1.0)
    }
    
    class func azure() -> UIColor {
        return UIColor(red: 0/255.0, green: 152.0/255.0, blue: 238.0/255.0, alpha: 1.0)
    }

    class func charcoalGrey() -> UIColor {
        return UIColor(red: 62.0/255.0, green: 62.0/255.0, blue: 73.0/255.0, alpha: 1.0)
    }

    class func coolGrey() -> UIColor {
        return UIColor(red: 177.0/255.0, green: 184.0/255.0, blue: 189.0/255.0, alpha: 1.0)
    }
    

}
